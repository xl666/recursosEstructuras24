import math

print(round(1.3))  # Redondea al numero entero mas cercani
print(round(1.7))
print(round(1.5))
print(abs(-55))  # Devuelve ek valor absoluto
print(math.ceil(1.1))  # Redondea al numero mas alto cercano
print(math.floor(1.9999))  # Redondea al numero mas bajo cercano
print(math.isnan(23))  # Devuelve si el valor ingresado es un numero o no
print(math.pow(5, 3))  # Eleva a la potencia, el numero a elevar es el izq
print(math.sqrt(49))   # Calcula la raiz cuadrada de un numero

