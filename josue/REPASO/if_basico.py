def saber_mes(n_mes):

    if n_mes == 1:
        print('enero')
    if n_mes == 2:
        print('febrero')
    if n_mes == 3:
        print('marzo')
    if n_mes == 4:
        print('abril')
    if n_mes == 5:
        print('mayo')
    if n_mes == 6:
        print('junio')
    if n_mes == 7:
        print('julio')
    if n_mes == 8:
        print('agosto')
    if n_mes == 9:
        print('septiembre')
    if n_mes == 10:
        print('octubre')
    if n_mes == 11:
        print('noviembre')
    if n_mes == 12:
        print('diciembre')

if __name__ == '__main__':
    n_mes = int(input())
    saber_mes(n_mes)
