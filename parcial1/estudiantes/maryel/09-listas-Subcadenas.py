Paises = ["Mexico",  "Estados Unidos", "Canada", "Chile"]
print (Paises[2])
Paises[2] = "India"#Modifica el valor de una posicion especifica del arreglo.
print (Paises)
print (Paises[-1])#Se puede acceder  al ultimo elemento de la lista con -1
print (Paises [1:3]) #Accede a un rango de elementos, en este caso desde el segundo hasta el tercero
print (Paises [:3])#Si no se indica inicio, toma desde el primer elemento hasta el el indice marcado
print (Paises [1:])#Si no se indica final, toma todos los elemntos desde el indice marcado hasta el final