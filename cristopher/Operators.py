#Arithmetic Operators
x, y = 2, 3

suma = x + y
resta = x - y 
Multiplicacion = x * y
Divison = x / y

print(f'suma {suma}, resta {resta}, multiplicacion {Multiplicacion}, division {Divison}')

#Assignment Operators 
x =  4
x += 2
print(x)
x *= 3
print(x)

#Unary Operator

n = 7
print(n)
n = -n
print(n)

#Relational Operators 
x, y = 1, 2 
print(x < y)
print(x <= y)
print(x >= y)

